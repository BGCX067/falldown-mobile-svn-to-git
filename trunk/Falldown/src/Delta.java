
public class Delta 
{
	int tousLes;
	double varSpeed;
	int pendant;
	
	public Delta(int tousLes, double varSpeed, int pendant)
	{
		this.tousLes = tousLes;
		this.varSpeed = varSpeed;
		this.pendant = pendant;
	}

	public int getTousLes() {
		return tousLes;
	}

	public double getVarSpeed() {
		return varSpeed;
	}

	public int getPendant() {
		return pendant;
	}
	
	
}
